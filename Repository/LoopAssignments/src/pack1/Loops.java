package pack1;

public class Loops {

	public static void main(String[] args) {
		
		for(int f=0; f<=5;f++) {
			System.out.println("f = "+f);
		}
		
		int w=0;
		while(w!=5)
			System.out.println(++w);
		
		
		int y = 0;
		do {
			System.out.println("print this");
		}
		while(y!=0);
		
		
	}
	

}
